package behavioral_design_patterns.command_pattern.example_1;

public class Light {

    public void on(){
        System.out.println("Light on");
    }

    public void off(){
        System.out.println("Light off");
    }

    public void up(){
        System.out.println("light up");
    }

    public void down(){
        System.out.println("Light down");
    }
}
